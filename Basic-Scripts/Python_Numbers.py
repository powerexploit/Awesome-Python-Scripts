# There are three types of number of in Python
# int,float,complex

print("Input :")
x = 15  # int
y = 14.5  # float
z = 1j  # complex
print("x = 15, y = 14.5, z = 1j")

# type() function is used to determine the type of variable
print("Output :")
print(type(x))
print(type(y))
print(type(z))

